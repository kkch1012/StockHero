import { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
  const baseUrl = process.env.NEXT_PUBLIC_SITE_URL || 'https://stockhero.app';

  return {
    rules: [
      {
        userAgent: '*',
        allow: '/',
        disallow: [
          '/api/',
          '/admin/',
          '/mypage/',
          '/subscription/success',
          '/subscription/cancel',
        ],
      },
    ],
    sitemap: `${baseUrl}/sitemap.xml`,
  };
}
